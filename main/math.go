package main

import "fmt"

func main() {

	a := 9
	b := 3

	sum := a + b
	sub := a - b
	mul := a * b
	div := a / b

	fmt.Println("Addition of two numbers : ", sum)
	fmt.Println("Substraction of two numbers : ", sub)
	fmt.Println("Multiplication of two numbers : ", mul)
	fmt.Println("Devision of two numbers : ", div)

	// Addition of two numbers :  12
	// Substraction of two numbers :  6
	// Multiplication of two numbers :  27
	// Devision of two numbers :  3
}
